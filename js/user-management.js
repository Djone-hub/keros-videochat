/**
 * Включает режим просмотра сайта от лица обычного пользователя.
 * Сохраняет текущую сессию администратора, выходит из системы и перезагружает страницу.
 */
async function enterUserViewMode() {
    if (confirm('Вы уверены, что хотите перейти в режим просмотра от лица пользователя? Админ-панель будет скрыта, но вы останетесь авторизованы.')) {
        console.log('👁️‍🗨️ Вход в режим просмотра от лица пользователя...');
        sessionStorage.setItem('isUserViewMode', 'true'); // Просто устанавливаем флаг
        window.location.reload(); // и перезагружаем страницу
    }
}

// Экспортируем функцию в глобальную область видимости
window.enterUserViewMode = enterUserViewMode;